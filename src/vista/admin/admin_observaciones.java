    DefaultTableModel modelo;
    private void CrearModelo() {
        try {
            modelo = (new DefaultTableModel(
                    null, new String[]{
                        "Codigo", "Empleado", "Mensaje", "Fecha"}) {
                Class[] types = new Class[]{
                    java.lang.String.class,
                    java.lang.String.class,
                    java.lang.String.class,
                    java.lang.String.class
                };
                boolean[] canEdit = new boolean[]{
                    false, false, false, false
                };

                @Override
                public Class getColumnClass(int columnIndex) {
                    return types[columnIndex];
                }

                @Override
                public boolean isCellEditable(int rowIndex, int colIndex) {
                    return canEdit[colIndex];
                }
            });
            tabla.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString() + "error");
            System.out.println("Problema con el modelo de tabla");
        }
    }
    
    
    private void Cargar_Informacion(){
        try{
            Object o[]=null;
            List<Observaciones> listP = cObservaciones.findObservacionesEntities();
            int contadorr=0;
            for (int i=0; i< listP.size(); i++){
                    Empleados obs = listP.get(i).getEmpDni();
                    modelo.addRow(o);
                    modelo.setValueAt(listP.get(i).getObsCodigo(), contadorr, 0);
                    modelo.setValueAt(obs.getEmpDni(), contadorr, 1);
                    modelo.setValueAt(listP.get(i).getObsDescripcion(), contadorr, 2);
                    modelo.setValueAt(darFormato(listP.get(i).getObsFecha()), contadorr, 3);
                    contadorr++;
            }                                            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
            System.out.println("problema al cargar datos");
        }
    }
    
    
